// while 
let index = 0
while (index<=10) {
    //console.log(`Value of index is ${index}`);
    index = index+2
}

let arr = ["ravi","shivam","tushar","naveen"]
let first = 0

while (first<arr.length) {
    //console.log(`value is ${arr[first]}`);
    first = first+1
}

//do while loop

let score = 11

do {
    console.log('Score is ',score);
    score++
} while (score<=10);

